package net.minecraft.src;

public class DecoUtilsColorizer 
{
	/**
     * Gets the foliage color for ebonwood type (metadata 0) trees
     */
    public static int getFoliageBloodwood()
    {
        return 14163743;
    }
	
	/**
     * Gets the foliage color for ebonwood type (metadata 0) trees
     */
    public static int getFoliageEbonwood()
    {
        return Integer.parseInt("FFA0FF", 16);
    }
	
	/**
     * Gets the foliage color for ironwood type (metadata 1) trees
     */
    public static int getFoliageIronwood()
    {
        return Integer.parseInt("4E5B27", 16);
    }
}
